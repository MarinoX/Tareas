    private void cargarTareas() {
    try {
        Connection conn = conexion.conn();
        if (conn != null) {
            String query = "SELECT * FROM tarea";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
            DefaultTableModel modelo1 = (DefaultTableModel) jTable3.getModel();
            modelo.setRowCount(0);
            modelo1.setRowCount(0);

            while (rs.next()) {
                Object[] fila = new Object[5];
                fila[0] = rs.getInt("id");
                fila[1] = rs.getString("nombre");
                fila[2] = rs.getString("descripcion");
                fila[3] = rs.getString("status");

                String fechaRaw = rs.getString("fecha");
                if (fechaRaw != null && !fechaRaw.equals("0000-00-00")) {
                    LocalDate fechaLocal = LocalDate.parse(fechaRaw);
                    String fechaStr = fechaLocal.format(DateTimeFormatter.ofPattern("yyyy-MM-dd"));
                    fila[4] = fechaStr;
                } else {
                    fila[4] = "";
                }

                modelo.addRow(fila);
                modelo1.addRow(fila);
            }

            conn.close();

            // Asignar el TableRowSorter solo a jTable2 (para filtros)
            //TableRowSorter<TableModel> sorter = new TableRowSorter<>(modelo);
            //jTable2.setRowSorter(sorter);

        } else {
            JOptionPane.showMessageDialog(null, "No se pudo conectar a la base de datos");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al cargar las tareas: " + e.getMessage());
    }
}

    
