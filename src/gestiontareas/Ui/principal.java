    private void btnAgregartareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregartareaActionPerformed
        // TODO add your handling code here:
        
        Tarea tarea = new Tarea();
        tarea.setNombre(txtNombre.getText());
        tarea.setDescripcion(txtDescripcion.getText());
        try {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            LocalDate fechaLimite = LocalDate.parse(txtFecha.getText(), formatter);
            tarea.setLimite(fechaLimite);
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(null, "Formato de fecha inválido. Usa yyyy-MM-dd");
            return;  // Para evitar continuar si la fecha es incorrecta
        }
        tarea.setStatus(comboEstado.getSelectedItem().toString());
        tareasDb tareasDb = new tareasDb();

        // Guardar tarea en DB
        if (tareasDb.RegistrarTarea(tarea)) {
            JOptionPane.showMessageDialog(null, "Tarea registrada correctamente.");
        } else {
            JOptionPane.showMessageDialog(null, "Error al registrar tarea.");
        }
        limpiarCamposAgregar();   
    }//GEN-LAST:event_btnAgregartareaActionPerformed

    private void btnEliminarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTareaActionPerformed
        // TODO add your handling code here:
    int filaSeleccionada = jTable3.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Por favor selecciona una tarea para eliminar.");
        return;
    }

    int id = (int) jTable3.getValueAt(filaSeleccionada, 0);

    int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este tarea?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
    if (confirmacion != JOptionPane.YES_OPTION) {
        return;
    }

    try {
        Connection conn = conexion.conn();
        String sql = "DELETE FROM tarea WHERE id = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, id);
        int filasAfectadas = ps.executeUpdate();

        if (filasAfectadas > 0) {
            JOptionPane.showMessageDialog(this, "tarea eliminada correctamente.");

            //Recargar la tabla
            //recargarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar esta tarea.");
        }

        ps.close();
        conn.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al eliminar la tarea: " + e.getMessage());
    }
    cargarTareas();   
    }//GEN-LAST:event_btnEliminarTareaActionPerformed

